import fs from 'node:fs/promises';
import { BetaAnalyticsDataClient } from '@google-analytics/data';

const propertyId = process.env.GA_PROPERTY_ID || '392399720';
const serviceAccountJson = process.env.GA_SERVICE_ACCOUNT_JSON;

if (!serviceAccountJson) {
  throw new Error('Missing GA_SERVICE_ACCOUNT_JSON. Add it as a GitHub Actions repository secret.');
}

let credentials;
try {
  credentials = JSON.parse(serviceAccountJson);
} catch (error) {
  throw new Error('GA_SERVICE_ACCOUNT_JSON is not valid JSON. Paste the full service account JSON into the GitHub secret.');
}

const analyticsDataClient = new BetaAnalyticsDataClient({ credentials });

const today = new Date();
const endDate = today.toISOString().slice(0, 10);
const startDate = new Date(today);
startDate.setDate(startDate.getDate() - 7);
const startDateText = startDate.toISOString().slice(0, 10);

async function runReport({ dimensions = [], metrics = [], limit = 10 }) {
  const [response] = await analyticsDataClient.runReport({
    property: `properties/${propertyId}`,
    dateRanges: [{ startDate: startDateText, endDate }],
    dimensions,
    metrics,
    limit,
  });

  return response.rows || [];
}

function metricValue(row, index = 0) {
  const value = row?.metricValues?.[index]?.value;
  if (value === undefined || value === null) return null;
  const numberValue = Number(value);
  return Number.isNaN(numberValue) ? value : numberValue;
}

function dimensionValue(row, index = 0) {
  return row?.dimensionValues?.[index]?.value ?? null;
}

const [summaryRows, pageRows, sourceRows, locationRows, deviceRows] = await Promise.all([
  runReport({
    metrics: [
      { name: 'activeUsers' },
      { name: 'newUsers' },
      { name: 'sessions' },
      { name: 'screenPageViews' },
      { name: 'engagedSessions' },
      { name: 'averageSessionDuration' },
    ],
    limit: 1,
  }),
  runReport({
    dimensions: [{ name: 'pagePath' }, { name: 'pageTitle' }],
    metrics: [{ name: 'screenPageViews' }, { name: 'activeUsers' }],
    limit: 10,
  }),
  runReport({
    dimensions: [{ name: 'sessionSourceMedium' }],
    metrics: [{ name: 'sessions' }, { name: 'activeUsers' }],
    limit: 10,
  }),
  runReport({
    dimensions: [{ name: 'country' }, { name: 'city' }],
    metrics: [{ name: 'activeUsers' }, { name: 'sessions' }],
    limit: 10,
  }),
  runReport({
    dimensions: [{ name: 'deviceCategory' }],
    metrics: [{ name: 'activeUsers' }, { name: 'sessions' }],
    limit: 10,
  }),
]);

const summaryRow = summaryRows[0];
const report = {
  site: 'https://inthesneakers.life/',
  platform: 'Google Analytics 4',
  measurement_id: 'G-XNFNVGXCFL',
  ga4_property_id: propertyId,
  date_range: {
    start_date: startDateText,
    end_date: endDate,
  },
  summary: {
    users: metricValue(summaryRow, 0),
    new_users: metricValue(summaryRow, 1),
    sessions: metricValue(summaryRow, 2),
    screen_page_views: metricValue(summaryRow, 3),
    engaged_sessions: metricValue(summaryRow, 4),
    average_session_duration_seconds: metricValue(summaryRow, 5),
  },
  top_pages: pageRows.map((row) => ({
    path: dimensionValue(row, 0),
    title: dimensionValue(row, 1),
    views: metricValue(row, 0),
    users: metricValue(row, 1),
  })),
  traffic_sources: sourceRows.map((row) => ({
    source_medium: dimensionValue(row, 0),
    sessions: metricValue(row, 0),
    users: metricValue(row, 1),
  })),
  locations: locationRows.map((row) => ({
    country: dimensionValue(row, 0),
    city: dimensionValue(row, 1),
    users: metricValue(row, 0),
    sessions: metricValue(row, 1),
  })),
  devices: deviceRows.map((row) => ({
    device: dimensionValue(row, 0),
    users: metricValue(row, 0),
    sessions: metricValue(row, 1),
  })),
  notes: [
    'This file was generated by scripts/fetch-analytics.mjs using the Google Analytics Data API.',
    'Date range is the last 7 days including today.',
  ],
  last_updated: new Date().toISOString(),
};

function tableRows(items, columns) {
  if (!items.length) return 'No data available yet.\n';
  const header = `| ${columns.map((column) => column.label).join(' | ')} |`;
  const divider = `| ${columns.map(() => '---').join(' | ')} |`;
  const rows = items.map((item) => `| ${columns.map((column) => item[column.key] ?? '—').join(' | ')} |`);
  return [header, divider, ...rows].join('\n') + '\n';
}

const summaryMarkdown = `# Google Analytics Summary

**Site:** https://inthesneakers.life/  
**Platform:** Google Analytics 4  
**Measurement ID:** \`G-XNFNVGXCFL\`  
**GA4 Property ID:** \`${propertyId}\`  
**Last updated:** ${report.last_updated}

## Date range

${startDateText} through ${endDate}

## Main numbers

| Metric | Value |
|---|---:|
| Users | ${report.summary.users ?? '—'} |
| New users | ${report.summary.new_users ?? '—'} |
| Sessions | ${report.summary.sessions ?? '—'} |
| Page views | ${report.summary.screen_page_views ?? '—'} |
| Engaged sessions | ${report.summary.engaged_sessions ?? '—'} |
| Average session duration | ${report.summary.average_session_duration_seconds ?? '—'} seconds |

## Top pages

${tableRows(report.top_pages, [
  { key: 'path', label: 'Path' },
  { key: 'title', label: 'Title' },
  { key: 'views', label: 'Views' },
  { key: 'users', label: 'Users' },
])}

## Traffic sources

${tableRows(report.traffic_sources, [
  { key: 'source_medium', label: 'Source / Medium' },
  { key: 'sessions', label: 'Sessions' },
  { key: 'users', label: 'Users' },
])}

## Locations

${tableRows(report.locations, [
  { key: 'country', label: 'Country' },
  { key: 'city', label: 'City' },
  { key: 'users', label: 'Users' },
  { key: 'sessions', label: 'Sessions' },
])}

## Devices

${tableRows(report.devices, [
  { key: 'device', label: 'Device' },
  { key: 'users', label: 'Users' },
  { key: 'sessions', label: 'Sessions' },
])}

## What this means

Ask ChatGPT to read this file and explain the traffic in plain English.
`;

await fs.writeFile('analytics-report.json', `${JSON.stringify(report, null, 2)}\n`);
await fs.writeFile('analytics-summary.md', summaryMarkdown);

console.log('Analytics files updated.');
